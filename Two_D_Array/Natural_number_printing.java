package Two_D_Array;

public class Natural_number_printing {
    public static void main(String[] args) {
        int count=0;
        for (int i=1;i<=5;i++){
            for (int j=1;j<=6;j++){
                count++;
                System.out.print(count+" ");
            }
            System.out.println();
        }

    }
}
