package BasicsOfJava;

public class swaping {
    public static void main(String[] args) {
        int a=5;
        int b=6;
        System.out.println("The value of a is : "+a);
        System.out.println("The value of b is :"+b);
        int temp=a;
        a=b;
        b=temp;
        System.out.println("The value of a is : "+a);
        System.out.println("The value of b is :"+b);
    }
}
